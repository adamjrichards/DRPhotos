						<h3>Bio for the Roselawn show</h3>						
						<p>Derek Richards was born in Montreal in 1937 and very early began to show a sense of the pictorial. He still has visual impressions of that city from the age of two. When his father enlisted in the RCAF, the family moved to the picturesque Eastern Townships where Derek was given a Brownie camera and picked up a childhood interest in landscape and nature photography. His favourite picture books at the time were illustrated by engravings of scenic subjects. Moving to New Brunswick and Europe with a father in the armed forces, he continued an interest in photography, experimenting with darkroom print-making, colour slides, and had a brief fling at free-lance photography.</p>
						<p>Married, with 4 children, and after a period in Toronto working for the Ministry of the Environment, Derek eventually settled in St. Catharines, in 1972, to work in computers at the Lincoln County Board of Education.</p>
						A<p>t that time his photographic interests were chiefly absorbed by making 35mm slide shows. Print-making remained a fond hope, but despite the acquisition of darkroom facilities, proved to be too unproductive for a photographer with a scarcity of time and resources.</p>
						<p>However, Derek's experience in computers and small-scale computer publishing fostered an interest in digital illustration and printing. That youthful ambition to do chemical print-making has now come full circle with a new technology that provides all the facilities and techniques of the early photo darkroom, but in a
						context that increases productivity and brings the photo artist ever closer to achieving the interpretive freedom of water-colourists and other graphic artists.</p>
						<p>Derek's first show, "Hello, World!", at the Roselawn in Port Colborne, was intended to be a celebration of the visual world and a tribute to the emergence of these new opportunities in digital print-making for all visual
						artists.</p>
